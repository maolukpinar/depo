package gun3;

public class Float {
    public static void main(String[] args) {
        float f = 10.1234567f; //noktadan sonra en fazla 6 değer alabiliyor.
        System.out.println(f);

        float f2 = 10.05F;// sayının sonuna büyük veya küçük f yazmamız gerekiyor
        double sum = 2;

        System.out.println(f2);
        System.out.println(sum);
    }
}
